// 2進数
// 0b + 2進数の値
const binX: number = 0b1010; // 10

// 8進数
// 0o + 8進数の値
const octX: number = 0o12; // 10

// 10進数
const decX: number = 10; // 10

// 16進数
// 0x + 16進数の値
const hexX: number = 0xa; // 10

console.log("binX : 1010 =", binX);
console.log("octX : 12   =", octX);
console.log("decX : 10   =", decX);
console.log("hexX : a    =", hexX);